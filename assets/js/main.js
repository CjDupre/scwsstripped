function hoverFeatureBox() {
	document.getElementById("title").style.color = "#71bde4";
	document.getElementById("feature-box").style.background = "transparent";
	document.getElementById("feature-box").style.cursor = "pointer";
	document.getElementById("feature-p").style.color = "#71bde4";
}
function OutHoverFeatureBox() {
	document.getElementById("title").style.color = "#71bde4";
	document.getElementById("feature-box").style.background =
		"linear-gradient(#b392b2 0%, #766287 50%, #372d4c 100%)";
	document.getElementById("feature-box").style.cursor = "pointer";
	document.getElementById("feature-p").style.color = "#71bde4";
}
